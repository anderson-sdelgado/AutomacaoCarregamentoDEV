    public void ok() {
        switch (this.baseJFrame.getAutomacaoCTR().okCPF()) {
            case "TelaDadosNEnc":
                this.baseJFrame.mudarTela("TelaDadosNEncJPanel");
                break;
            case "TelaDadosUltCarreg":
                this.baseJFrame.mudarTela("TelaDadosUltCarregJPanel");
                break;
            case "TelaCamCPreOrdem":
                this.baseJFrame.mudarTela("TelaCamCPreOrdemJPanel");
                break;
            case "CPF INVÁLIDO":
                cpfInvalido();
                break;
            case "CPF ABERTO":
                cpfAberto();
                break;
        }
    }

    public void cpfInvalido() {
        defaultJDialog.setTxtMsg("CPF INVÁLIDO.\nPor favor, digite novamente o CPF.");
        Timer timer = new Timer(4000, new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                defaultJDialog.setVisible(false);
            }
        });
        timer.setRepeats(false);
        timer.start();
        defaultJDialog.setVisible(true);
    }

    public void cpfAberto() {
        defaultJDialog.setTxtMsg("Por Favor, termine de digitar o CPF.");
        Timer timer = new Timer(4000, new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                defaultJDialog.setVisible(false);
            }
        });
        timer.setRepeats(false);
        timer.start();
        defaultJDialog.setVisible(true);
    }

    @Override
    public Dimension getPreferredSize() {
        Dimension superSize = super.getPreferredSize();
        if (isPreferredSizeSet() || image == null) {
            return superSize;
        }
        int prefW = Math.max(image.getWidth(null), superSize.width);
        int prefH = Math.max(image.getHeight(null), superSize.height);
        return new Dimension(prefW, prefH);
    }

    @Override
    protected void paintComponent(Graphics g) {
        super.paintComponent(g);
        if (image != null) {
            g.drawImage(image, 0, 0, null);
        }
    }

