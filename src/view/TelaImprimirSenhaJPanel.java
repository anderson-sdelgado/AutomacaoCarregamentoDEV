    public void exibir() {
        
        jLabelSenha.setText(this.baseJFrame.getAutomacaoCTR().getSenha());
        jLabelStatus.setText("STATUS: " + this.baseJFrame.getAutomacaoCTR().getDescrEtapa());
        jLabelMsg.setText(this.baseJFrame.getAutomacaoCTR().msgImprimir().replace(".", ".\n").toUpperCase());
        jLabelMsg.setLineWrap(true);

        this.baseJFrame.getAutomacaoCTR().imprimir();

    }

    private void timerFecharTela() {
        Timer t = new Timer(30000, (ActionEvent e) -> {
            fecharTela();
        });
        t.start();
    }

    private void fecharTela() {
        if (!fechaTela) {
            fechaTela = true;
            this.baseJFrame.mudarTela(Const.TELA_CPF);
        }
    }

    @Override
    public Dimension getPreferredSize() {
        Dimension superSize = super.getPreferredSize();
        if (isPreferredSizeSet() || image == null) {
            return superSize;
        }
        int prefW = Math.max(image.getWidth(null), superSize.width);
        int prefH = Math.max(image.getHeight(null), superSize.height);
        return new Dimension(prefW, prefH);
    }

    @Override
    protected void paintComponent(Graphics g) {
        super.paintComponent(g);
        if (image != null) {
            g.drawImage(image, 0, 0, null);
        }
    }

